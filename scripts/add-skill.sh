    echo "üîß –°–æ–∑–¥–∞–Ω–∏–µ –Ω–æ–≤–æ–≥–æ –Ω–∞–≤—ã–∫–∞ –¥–ª—è Claude"
    echo ""

    # –í–≤–æ–¥ –¥–∞–Ω–Ω—ã—Ö
    local skill_name
    local skill_description
    local skill_category
    local create_extras

    while true; do
        skill_name=$(prompt "–ù–∞–∑–≤–∞–Ω–∏–µ –Ω–∞–≤—ã–∫–∞ (–ª–∞—Ç–∏–Ω–∏—Ü–∞, –¥–µ—Ñ–∏—Å—ã)" "")

        if [[ -z "$skill_name" ]]; then
