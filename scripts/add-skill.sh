set -euo pipefail

# –ü—É—Ç–∏
REPO_SKILLS="$HOME/claude-skills/skills"
SCRIPTS_DIR="$HOME/claude-skills/scripts"

# –¶–≤–µ—Ç–∞
GREEN='\033[0;32m'
BLUE='\033[0;34m'
YELLOW='\033[0;33m'
RED='\033[0;31m'
NC='\033[0m'

log_info() {
    echo -e "${BLUE}‚Ñπ${NC} $1"
}

log_success() {
    echo -e "${GREEN}‚úì${NC} $1"
}

log_warn() {
    echo -e "${YELLOW}‚ö†${NC} $1"
}

# –ò–Ω—Ç–µ—Ä–∞–∫—Ç–∏–≤–Ω—ã–π –≤–≤–æ–¥ —Å –ø–æ–¥—Å–∫–∞–∑–∫–æ–π
prompt() {
    local prompt_text="$1"
    local default_value="${2:-}"
    local result

    if [[ -n "$default_value" ]]; then
        read -rp "$prompt_text [$default_value]: " result
        echo "${result:-$default_value}"
    else
        read -rp "$prompt_text: " result
        echo "$result"
    fi
}

# –í–∞–ª–∏–¥–∞—Ü–∏—è –∏–º–µ–Ω–∏ –Ω–∞–≤—ã–∫–∞
validate_skill_name() {
    local name="$1"

    # –ü—Ä–æ–≤–µ—Ä–∫–∞ —Ñ–æ—Ä–º–∞—Ç–∞: —Ç–æ–ª—å–∫–æ –ª–∞—Ç–∏–Ω–∏—Ü–∞, —Ü–∏—Ñ—Ä—ã, –¥–µ—Ñ–∏—Å—ã
    if [[ ! "$name" =~ ^[a-z0-9-]+$ ]]; then
        log_error "–ò–º—è –Ω–∞–≤—ã–∫–∞ –¥–æ–ª–∂–Ω–æ —Å–æ–¥–µ—Ä–∂–∞—Ç—å —Ç–æ–ª—å–∫–æ —Å—Ç—Ä–æ—á–Ω—ã–µ –ª–∞—Ç–∏–Ω—Å–∫–∏–µ –±—É–∫–≤—ã, —Ü–∏—Ñ—Ä—ã –∏ –¥–µ—Ñ–∏—Å—ã"
        return 1
    fi

    # –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ —Å—É—â–µ—Å—Ç–≤–æ–≤–∞–Ω–∏–µ
    if [[ -d "$REPO_SKILLS/$name" ]]; then
        log_error "–ù–∞–≤—ã–∫ —Å —Ç–∞–∫–∏–º –∏–º–µ–Ω–µ–º —É–∂–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç"
        return 1
    fi

    return 0
}

# –°–æ–∑–¥–∞–Ω–∏–µ —à–∞–±–ª–æ–Ω–∞ SKILL.md
