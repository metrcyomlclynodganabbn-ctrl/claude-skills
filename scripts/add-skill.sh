\`\`\`

**–í—ã—Ö–æ–¥:**
\`\`\`
[–ü—Ä–∏–º–µ—Ä –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö]
\`\`\`

---

## –ü—Ä–∏–º–µ—Ä 2: [–ù–∞–∑–≤–∞–Ω–∏–µ]

**–í—Ö–æ–¥:**
\`\`\`
[–ü—Ä–∏–º–µ—Ä –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö]
\`\`\`

**–í—ã—Ö–æ–¥:**
\`\`\`
[–ü—Ä–∏–º–µ—Ä –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö]
\`\`\`
