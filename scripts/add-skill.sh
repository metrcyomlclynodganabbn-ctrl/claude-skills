# –°–∏–Ω—Ö—Ä–æ–Ω–∏–∑–∞—Ü–∏—è –Ω–æ–≤–æ–≥–æ –Ω–∞–≤—ã–∫–∞
sync_new_skill() {
    local name="$1"

    log_info "–°–∏–Ω—Ö—Ä–æ–Ω–∏–∑–∞—Ü–∏—è —Å Claude Code..."

    "$SCRIPTS_DIR/sync-skills.sh" sync "$name" >/dev/null 2>&1 || {
        log_warn "–ù–µ —É–¥–∞–ª–æ—Å—å –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ —Å–∏–Ω—Ö—Ä–æ–Ω–∏–∑–∏—Ä–æ–≤–∞—Ç—å"
        log_info "–ó–∞–ø—É—Å—Ç–∏—Ç–µ –≤—Ä—É—á–Ω—É—é: $SCRIPTS_DIR/sync-skills.sh sync $name"
        return 0
    }

    log_success "–ù–∞–≤—ã–∫ –ø–æ–¥–∫–ª—é—á—ë–Ω –∫ Claude Code"
}

# –ì–ª–∞–≤–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è
main() {
