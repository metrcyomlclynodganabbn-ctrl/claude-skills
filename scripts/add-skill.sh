            continue
        fi

        if validate_skill_name "$skill_name"; then
            break
        fi
    done

    skill_description=$(prompt "–ö—Ä–∞—Ç–∫–æ–µ –æ–ø–∏—Å–∞–Ω–∏–µ" "–ù–æ–≤—ã–π –Ω–∞–≤—ã–∫ Claude")
    skill_category=$(prompt "–ö–∞—Ç–µ–≥–æ—Ä–∏—è" "custom")

    # –°–æ–∑–¥–∞–Ω–∏–µ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏
    local skill_dir="$REPO_SKILLS/$skill_name"
    mkdir -p "$skill_dir"

    log_info "–°–æ–∑–¥–∞–Ω–∏–µ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏: $skill_dir"

    # –°–æ–∑–¥–∞–Ω–∏–µ –æ—Å–Ω–æ–≤–Ω–æ–≥–æ —Ñ–∞–π–ª–∞
    create_skill_template "$skill_name" "$skill_description" "$skill_category"

    # –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–µ —Ñ–∞–π–ª—ã
    create_extras=$(prompt "–°–æ–∑–¥–∞—Ç—å reference.md –∏ examples.md?" "y")

    if [[ "$create_extras" =~ ^[Yy]$ ]]; then
        create_extra_files "$skill_name"
    fi

    # –°–∏–Ω—Ö—Ä–æ–Ω–∏–∑–∞—Ü–∏—è
    echo ""
    sync_new_skill "$skill_name"

    echo ""
    log_success "–ù–∞–≤—ã–∫ —Å–æ–∑–¥–∞–Ω!"
    echo ""
    echo "üìÅ –†–∞—Å–ø–æ–ª–æ–∂–µ–Ω–∏–µ: $skill_dir"
    echo "üìù –û—Å–Ω–æ–≤–Ω–æ–π —Ñ–∞–π–ª: $skill_dir/SKILL.md"
    echo ""
    echo "–°–ª–µ–¥—É—é—â–∏–µ —à–∞–≥–∏:"
    echo "   1. –û—Ç–∫—Ä–æ–π—Ç–µ SKILL.md –∏ –∑–∞–ø–æ–ª–Ω–∏—Ç–µ —Å–æ–¥–µ—Ä–∂–∏–º–æ–µ"
    echo "   2. –ü—Ä–æ—Ç–µ—Å—Ç–∏—Ä—É–π—Ç–µ –Ω–∞–≤—ã–∫ –≤ Claude Code"
    echo "   3. –ó–∞—Ñ–∏–∫—Å–∏—Ä—É–π—Ç–µ –∏–∑–º–µ–Ω–µ–Ω–∏—è –≤ git (–µ—Å–ª–∏ –Ω—É–∂–Ω–æ)"
    echo ""
}

