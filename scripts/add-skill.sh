create_extra_files() {
    local name="$1"
    local skill_dir="$REPO_SKILLS/$name"

    # reference.md ‚Äî —Ä–∞–∑–≤—ë—Ä–Ω—É—Ç—ã–µ –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏–∏
    cat > "$skill_dir/reference.md" << EOF
